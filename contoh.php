            <li>Seorang anak berusia 1,5 tahun tinggal di lingkungan kumuh dengan sanitasi buruk. Akibat sering terkena diare dan penyakit infeksi, ditambah pola makan yang tidak bergizi, pertumbuhan anak ini terhambat. Pada usia 2 tahun, tinggi badannya lebih rendah dari standar usianya, dan ia sering sakit, yang memperburuk kondisinya.</li>
            <li>Seorang anak berusia 4 tahun dari keluarga di pedesaan tidak diberikan makanan yang bervariasi. Orang tuanya hanya memberikan makanan pokok seperti nasi dan mi instan tanpa asupan protein, vitamin, atau mineral yang mencukupi. Anak ini tidak mendapatkan ASI eksklusif saat bayi, dan ketika diperiksa oleh petugas kesehatan, ditemukan bahwa tinggi badannya berada di bawah standar usianya, yang menunjukkan stunting.</li>
