        <h2>Cara Pencegahan Stunting</h2>
        <p>Pencegahan stunting dimulai dari masa kehamilan dan harus diteruskan selama masa pertumbuhan anak. Berikut adalah beberapa cara pencegahan yang efektif:</p>
        <li><strong>Gizi ibu hamil:</strong> Ibu hamil perlu mengonsumsi makanan sehat dan bergizi, serta mengonsumsi suplemen jika dianjurkan dokter. Ibu hamil juga perlu melakukan pemeriksaan kehamilan secara rutin, minimal 6 kali selama kehamilan.</li> 
        <li><strong>Gizi bayi:</strong> Bayi perlu diberikan ASI eksklusif hingga berusia 6 bulan. Setelah itu, bayi perlu diberikan makanan pendamping ASI (MPASI) yang mengandung gizi seimbang dan lengkap, terutama protein hewani.</li>
        <li><strong>Imunisasi:</strong> Anak perlu mendapatkan imunisasi lengkap.</li>
        <li><strong>Perkembangan anak:</strong> Anak perlu dipantau perkembangannya dan dibawa ke posyandu secara berkala.</li>
        <li><strong>Olahraga:</strong> Ibu hamil perlu berolahraga secara rutin untuk mendukung kehamilan yang sehat dan mengurangi risiko stunting pada janin.</li>
        <li><strong>Konsultasi dokter:</strong> Konsultasikan kepada dokter spesialis anak agar anak mendapatkan gizi yang sesuai.</li>
        <li><strong>Edukasi:</strong> Edukasi remaja untuk tidak mengonsumsi minuman beralkohol.</li>
