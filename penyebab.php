        <h2>Faktor Penyebab Stunting</h2>
        <p>Ada banyak faktor yang dapat menyebabkan anak mengalami stunting. Beberapa di antaranya adalah:</p>
        <ul>
            <li><strong>Kekurangan Gizi Kronis:</strong> Anak tidak mendapatkan nutrisi yang cukup, terutama protein, vitamin, dan mineral, sejak dalam kandungan hingga usia 2 tahun (1.000 hari pertama kehidupan).</li>
            <li><strong>Pola Makan yang Tidak Seimbang:</strong> Anak yang tidak menerima ASI eksklusif selama 6 bulan pertama, atau diberi makanan tambahan yang tidak mencukupi kebutuhan gizi, berisiko tinggi mengalami stunting.</li>
            <li><strong>Infeksi Berulang:</strong> Penyakit seperti diare, infeksi saluran pernapasan, dan infeksi cacing yang sering terjadi dapat mengganggu penyerapan gizi dan memperlambat pertumbuhan anak.</li>
            <li><strong>Kesehatan Ibu yang Buruk:</strong> Ibu yang mengalami kekurangan gizi selama kehamilan atau menyusui, atau menderita penyakit kronis, dapat mempengaruhi pertumbuhan anak dalam kandungan.</li>
            <li><strong>Sanitasi dan Kebersihan yang Buruk:</strong> Lingkungan yang tidak higienis dan kurangnya akses ke air bersih dapat meningkatkan risiko infeksi yang berulang, seperti diare, yang berdampak pada pertumbuhan anak.</li>
            <li><strong>Kurangnya Akses ke Layanan Kesehatan:</strong> Keterbatasan akses terhadap imunisasi, suplemen gizi, dan perawatan medis yang diperlukan selama kehamilan dan masa awal kehidupan anak juga berkontribusi pada stunting.</li>
            <li><strong>Kemiskinan dan Keterbatasan Pendidikan:</strong> Kondisi ekonomi yang sulit dan kurangnya pengetahuan orang tua tentang pentingnya gizi dan kesehatan anak meningkatkan risiko terjadinya stunting.</li>
        </ul>
